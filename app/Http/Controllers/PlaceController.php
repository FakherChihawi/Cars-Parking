     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'title' => 'required',
            'body' => 'required'
        ]);

        // create place
        $place = new place;
        $place->id = $request ->input('title');
        $place->etat = $request->input('body');
        $place->save;

        return redirect('/place')->with('seccuss', 'place created');
    }

